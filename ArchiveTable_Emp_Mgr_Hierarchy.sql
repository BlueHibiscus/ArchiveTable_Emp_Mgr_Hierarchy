/*CREATING STORED PROCEDURE WITH EQUAL NUMBER OF INPUT PARAMETERS AS THE COLUMNS  IN THE TABLE */ 

CREATE PROC MANIPULATION_PROC(@EMPLOYEEID INT,
							  @CARDNO VARCHAR(30),
							  @FIRSTNAME VARCHAR(30),
							  @LASTNAME VARCHAR(20),
							  @GENDER NCHAR(1),
							  @TITLE NVARCHAR(100),
							  @MANAGERID INT,
							  @HIREDATE DATE,
							  @SALARY MONEY,
							  @BIRTHDATE DATE,
							  @MARITALSTATUS NCHAR(1),
							  @DEPARTMENTID INT
							  )
AS
BEGIN

-- FEW VALIDATIONS CHECKING THE DATA INTEGRITY FOR AUTHENTIC DATA INSERTIONS AND AVOIDING INCORRECT DATA ENTRY --
  
		IF EXISTS(SELECT * FROM HUMANRESOURCE.MYEMPLOYEESDETAILS WHERE EMPLOYEEID = @EMPLOYEEID)
		BEGIN
		
		--OUTPUT COMMAND--
			PRINT 'EMPLOYEE WITH THIS NUMBER ALREADY EXISTS'
		
		--RETURN COMMAND CAUSES THE PROCEDURE TO EXIT AFTER A MESSAGE HAS BEEN SENT TO THE USER'S SCREEN-- 
			RETURN
		END

		
		--VALIDATIONS FOR CHECKING EMPLOYEEID CANNOT BE INSERTED WITH A NULL --
		
		IF @EMPLOYEEID IS NULL
		BEGIN
			PRINT 'PLEASE ENTER EMPLOYEE IDENTITY NUMBER'
			RETURN
		END

		--NO NEW DATA ENTRY IN BASE TABLE FOR EMPLOYEES HIRED ON OR BEFORE THE YEAR 2015 --
		
		IF YEAR(@HIREDATE) <= 2015
		BEGIN
			PRINT 'THIS YEAR CANNOT BE INSERTED'
			RETURN
		END

		/* TO BE A MANAGER THE PERSON MUST FIRST BE AN EMPLOYEE OF THE COMPANY
		   NEW DATA ENTRY IS ONLY POSSIBLE WHEN THE MANAGERID IS PRESENT IN THE EMPLOYEEID LIST  */
		   
		IF NOT EXISTS (SELECT * FROM HUMANRESOURCE.MYEMPLOYEESDETAILS
					   WHERE EMPLOYEEID = @MANAGERID)
		
		BEGIN
		    PRINT 'THIS MANAGER DOES NOT EXISTS'
			RETURN
		END


	--POPULATING OLDEST(TOP 1)  RECORDS INTO ARCHIVE TABLE FROM BASE TABLE OF EMPLOYEES
	--HIRED SORTING WITH THE YEAR,MONTH AND DAY IN ASCENDING ORDER

		INSERT HUMANRESOURCE.MYEMPLOYEESDETAILS_ARCH 
			SELECT * FROM HUMANRESOURCE.MYEMPLOYEESDETAILS
			  WHERE YEAR(HIREDATE) = (
									SELECT TOP 1 YEAR(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS 
									WHERE ACTIVE = 0 
									ORDER BY HIREDATE)
			  AND MONTH(HIREDATE) = (
									SELECT TOP 1 MONTH(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
									)

			  AND DAY(HIREDATE) = (
									SELECT TOP 1 DAY(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
									)
	
	--SIMULANEOUSLY, DELETING OLDEST(TOP 1)RECORDS FROM BASE TABLE OF EMPLOYEES--
	--HIRED SORTING WITH THE YEAR,MONTH AND DAY IN ASCENDING ORDER--
	
		DELETE FROM HUMANRESOURCE.MYEMPLOYEESDETAILS
			 WHERE YEAR(HIREDATE) =(
									SELECT TOP 1 YEAR(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
								   )
									
			 AND MONTH(HIREDATE) = (
									SELECT TOP 1 MONTH(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS 
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
								   )

			 AND DAY(HIREDATE) = (
									SELECT TOP 1 DAY(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS 
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
								   )

	--SIMULTANEOUSLY, DELETE THE OLDEST(TOP 1)RECORD FROM ARCHIVE TABLE WHEN A NEW RECORD --
	--IS INSERTED FROM BASE TABLE OF EMPLOYEES HIRED, SORTING WITH THE YEAR,MONTH AND DAY IN ASCENDING ORDER--
	
		DELETE  FROM HUMANRESOURCE.MYEMPLOYEESDETAILS_arch
			WHERE YEAR(Hiredate) =(
									SELECT TOP 1 YEAR(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS_ARCH
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
									)
									
			AND MONTH(HIREDATE) =  (
									SELECT TOP 1 MONTH(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS_ARCH 
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
									)

			AND DAY(HIREDATE) =  (
									SELECT TOP 1 DAY(HIREDATE)
									FROM HUMANRESOURCE.MYEMPLOYEESDETAILS_ARCH 
									WHERE ACTIVE = 0
									ORDER BY HIREDATE
									)

	--VALIDATING TO CHECK THE EXISTANCE OF A NEW EMPLOYEEID IN BASE TABLE--
		IF NOT EXISTS (SELECT * FROM HUMANRESOURCE.MYEMPLOYEESDETAILS 
		               WHERE EMPLOYEEID = @EMPLOYEEID
					  )

		BEGIN
	
	--INSERTING A NEW RECORD IN BASE TABLE--	
		INSERT HUMANRESOURCE.MYEMPLOYEESDETAILS VALUES
						  (	  
							  @EMPLOYEEID,
							  @CARDNO,
							  @FIRSTNAME,
							  @LASTNAME,
							  @GENDER,
							  @TITLE,
							  @MANAGERID,
							  @HIREDATE,
							  @SALARY,
							  @BIRTHDATE,
							  @MARITALSTATUS,
							  @DEPARTMENTID
						  )
	    PRINT 'RECORD INSERTED'
	
		END
END


