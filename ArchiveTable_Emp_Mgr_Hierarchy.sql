-------------------STEP 1:------------------
-- TO CHECK THE EXISTENCE OF THE PROCEDURE --

IF OBJECT_ID('FINDBOSS_PROC') IS NOT NULL 
DROP PROC FINDBOSS_PROC
GO

--TO CHECK EMPLOYEE MANAGER HIERARCHY --
/* EVERY NEW DATA ENTRY MADE IN BASE TABLE HAS VARIOUS MANIPULATIONS PERFORMED WITHIN
   BASE TABLE AND ARCHIVE TABLES SIMULTANEOUSLY. TO CAPTURE THE EMPLOYEE MANAGER HIERARCHY
   RECURSIONS AFTER EVERY TRANSACTIONS WITHIN EMPLOYEE AND THEIR RESPECTIVE MANAGERS 
   ARE CHECKED HERE. */
   
    
CREATE PROC FINDBOSS_PROC(@EMPLOYEEID INT)
AS
BEGIN
	DECLARE @MANAGERID INT,@FIRSTNAME VARCHAR(30),@LASTNAME VARCHAR(30)
	
	--SELF INNER JOIN WITHIN THE EMPLOYEES AND MANAGERS IN BASE TABLE-- 
	SELECT E.EMPLOYEEID EMPLOYEE_NO,
	       E.FIRSTNAME EMPLOYEE_FIRSTNAME,
		   E.LASTNAME EMPLOYEE_LASTNAME,
		   E1.EMPLOYEEID MANAGER_EMPLOYEE_NO,
		   E1.FIRSTNAME MANAGER_FIRSTNAME,
		   E1.LASTNAME MANAGER_LASTNAME
	FROM HUMANRESOURCE.MYEMPLOYEESDETAILS E 
	       INNER JOIN 
		 HUMANRESOURCE.MYEMPLOYEESDETAILS E1
	ON     E.MANAGERID = E1.EMPLOYEEID
	AND E.EMPLOYEEID =@EMPLOYEEID 

	SELECT @EMPLOYEEID=EMPLOYEEID,
	       @FIRSTNAME =FIRSTNAME,
		   @MANAGERID = MANAGERID
		   FROM HUMANRESOURCE.MYEMPLOYEESDETAILS 
		   WHERE EMPLOYEEID  = @EMPLOYEEID
		   

    --EACH TIME A STORED PROCEDURE CALLS ANOTHER STORED PROCEDURE--
	--THE NESTING LEVEL IS INCREMENTED--
	--WHEN THE MAXIMUM OF 32 IS EXCEEDED, THE TRANSACTION IS TEMINATED.--
	IF @MANAGERID IS NOT NULL AND @@NESTLEVEL <= 32
			BEGIN
				--SELECT @ENAME AS NAME,@Empno  AS Employee, @Mgr  AS Manager
 				EXEC FINDBOSS_PROC @MANAGERID
			END
END


-- EXECUTE THE STORED PROCEDURE--

EXEC FINDBOSS_PROC 303

